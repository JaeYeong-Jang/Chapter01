package com.javaex.ex01;

public class JavaEx05 {
	
	public static void main(String[]args) {
		
		int floor = 3;
		
		switch(floor) {
		
		case 1 : System.out.println("1층 약국 입니다.");
		break;
		
		case 2 : System.out.println("2층 정형외과 입니다.");
		break;
		
		case 3 : System.out.println("3층 피부과 입니다.");
		break;
		
		case 4 : System.out.println("4층 치과 입니다.");
		break;
		
		case 5 : System.out.println("5층 헬스클럽 입니다.");
		break;
		
		}
		
	}

}
